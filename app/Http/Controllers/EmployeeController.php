    public function employeeActivation($id)
    {
        $activationEmployee = User::find($id);

        if ($activationEmployee->is_verified == '1') {
           $activationEmployee->is_verified = '0';
            $activationEmployee->update();
            return "deactivated";

        }
        else{
            $activationEmployee->is_verified = '1';
            $activationEmployee->update();
            return "activated";
//            return true;
        }


    }
